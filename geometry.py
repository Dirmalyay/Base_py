import math

def square_round(r):
    return math.pi*(r**2)

def sqr_area(a,b):
    return a*b

def sqr_tiang(h,b):
    return (h*b)/2

def vol_sfere(r):
    return 4*(math.pi*(r**3))/3

def vol_conus(r,h):
    return (math.pi*(r**2)*h)/3

def vol_cude(a):
    return a**3

def vol_parall(a,b,h):
    return a*b*h
